import UIKit

var dayOfWeek = 4

switch dayOfWeek{
   case 1:
     print("saturday")
   case 2:
     print("sunday")
   case 3:
     print("monday")
   case 4:
     print("tuesday")
   case 5:
     print("wednesday")
   case 6:
     print("thursday")
   case 7:
     print("friday")
}

// output: tuesday

